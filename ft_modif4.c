#include "fillit.h"

char		**ft_modif4(char **tab)
{
	int y;
	int i;

	i = 3;
	y = 0;
	while (ft_strcmp(tab[0], "....") == 0)
	{
		while (y < 3)
		{
			tab[y] = ft_strdup(tab[y + 1]);
			y++;
		}
		y = 0;
		tab[i][0] = '\0';
		i--;
	}
	if (ft_strcmp(tab[3], "....") == 0)
		tab[3][0] = '\0';
	if (ft_strcmp(tab[2], "....") == 0)
		tab[2][0] = '\0';
	if (ft_strcmp(tab[1], "....") == 0)
		tab[1][0] = '\0';
	return (tab);
}
