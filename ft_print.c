#include "fillit.h"

void	ft_print(char **tab)
{
	int	y;

	y = 0;
	if (tab == 0)
	{
		ft_putstr("tab == 0");
	}
	else
		while (tab[y])
		{
			ft_putstr(tab[y]);
			ft_putstr("\n");
			y++;
		}
}
